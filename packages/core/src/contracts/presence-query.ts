import { z } from "zod";

/**
 * Query para endpoint de presencia (in/out)
 */
export const PresenceQuerySchema = z.object({
  merchant_id: z.string().regex(/^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$/),
  customer_id: z.string().regex(/^[0-9a-fA-F]{8}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{4}-[0-9a-fA-F]{12}$/),
});

export type PresenceQuery = z.infer<typeof PresenceQuerySchema>;
